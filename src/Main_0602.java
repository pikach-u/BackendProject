//0602 increment and decrement operator

public class Main_0602 {
    public static void main(String[] args) {
        int a = 10, b = 3;

        ++a;
        System.out.println(a);

        --b;
        System.out.println(b);

        a++;
        System.out.println(a);

        b--;
        System.out.println(b);

        System.out.println(++a);

        System.out.println(--b);

        System.out.println(a++);

        System.out.println(a);

        System.out.println(b--);

        System.out.println(b);
    }
}

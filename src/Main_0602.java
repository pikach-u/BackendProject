//0602 Operator

public class Main_0602 {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        int sum = a+b;
        System.out.println(sum);

        int difference = a-b;
        System.out.println(difference);

        int product = a*b;
        System.out.println(product);

        int quotient = a/b;
        System.out.println(quotient);

        int remainder = a%b;
        System.out.println(remainder);

    }
}
